public class NewsAdabter extends ArrayAdapter<News> {

    ViewHolder holder;

    public NewsAdabter(Activity context, ArrayList<News> newsadapter) {
        // Here, we initialize the ArrayAdapter's internal storage for the context and the list.
        // the second argument is used when the ArrayAdapter is populating a single TextView.
        // Because this is a custom adapter for two TextViews and an ImageView, the adapter is not
        // going to use this second argument, so it can be any value. Here, we used 0.
        super(context, 0, newsadapter);
    }

    public View getView(int position, View convertView, ViewGroup parent) {
        // Check if the existing view is being reused, otherwise inflate the view
        View listItemView = convertView;
        if(listItemView == null) {
            listItemView = LayoutInflater.from(getContext()).inflate(
                    R.layout.list_item, parent, false);
        }
        holder = new ViewHolder();
        holder.newsTitle = (TextView) listItemView.findViewById(R.id.text_title);
        holder.newsImage = (ImageView) listItemView.findViewById(R.id.thumbnail);

        News currentNews= getItem(position);

        holder.newsImage.setImageResource(R.drawable.defaultimage);
        new DownloadImageTask(holder.newsImage).execute(currentNews.getThumbnail());
        holder.newsTitle.setText(currentNews.getWebTitle());

        return listItemView;
    }


    private static class ViewHolder {
        ImageView newsImage;
        TextView newsTitle;
    }

    private class DownloadImageTask extends AsyncTask<String, Void, Bitmap> {
        ImageView bmImage;

        public DownloadImageTask(ImageView bmImage) {
            this.bmImage = bmImage;
        }
        protected Bitmap doInBackground(String... urls) {
            String urldisplay = urls[0];
            Bitmap mIcon = null;
            try {
                InputStream in = new java.net.URL(urldisplay).openStream();
                mIcon = BitmapFactory.decodeStream(in);
            } catch (Exception e) {
                Log.e("Error", e.getMessage());
                e.printStackTrace();
            }
            return mIcon;
        }

        protected void onPostExecute(Bitmap result) {
            bmImage.setImageBitmap(result);
        }
    }



}
